# ========== 详细解析版 ==========
# 初始化任务列表，是个空篮子
tasks = []

# 专门负责打印菜单的函数，让代码更整洁
def show_menu():
    print("\n=== 待办事项 ===")
    print("1. 添加任务")
    print("2. 查看任务")
    print("3. 退出")

# 主程序入口
def main():
    # 死循环，程序一直跑直到用户选退出
    while True:
        # 先展示菜单
        show_menu()
        # 获取用户选择
        choice = input("请选择操作 (1-3): ")
        
        # 如果选1，添加逻辑
        if choice == '1':
            task = input("请输入任务内容: ")
            # strip() 去除首尾空格，防止用户敲空格当任务
            if task.strip():
                tasks.append(task)
                print("✅ 任务已添加！")
            else:
                print("⚠️ 任务不能为空")
        
        # 如果选2，查看逻辑
        elif choice == '2':
            # 如果列表是空的
            if not tasks:
                print("📭 暂无任务，去添加一个吧！")
            else:
                # enumerate 可以同时拿到索引和内容
                # 1 表示索引从1开始计数
                for i, t in enumerate(tasks, 1):
                    print(f"{i}. {t}")
        
        # 如果选3，跳出循环，结束程序
        elif choice == '3':
            print("👋 退出程序，再见！")
            break
            
        # 防呆处理，输入了其他乱七八糟的东西
        else:
            print("⚠️ 输入无效，请重新输入！")

# 这是 Python 程序的标准启动方式
# 只有直接运行这个文件时，才会执行 main()
# 如果是被其他文件 import，则不会自动运行
if __name__ == "__main__":
    main()

# ========== 大白话解释 ==========
# 这就是一个小管家。
# tasks 是你的记事本。
# while 循环是管家一直在旁边等你吩咐。
# 你说“记一下”，他就掏出本子写；你说“念念”，他就读给你听。
# `if __name__ == "__main__":` 就像是管家的“上岗证”，只有是你亲自雇佣（运行）他时，他才开工。

