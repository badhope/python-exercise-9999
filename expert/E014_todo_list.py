# -----------------------------
# 题目：命令行待办事项工具。
# 描述：编写一个 CLI 工具，支持三个功能：添加任务、查看列表、退出。
#      要求：使用循环菜单，输入验证，友好提示。
#
# 示例：
# 选择操作：1. 添加  2. 查看  3. 退出
# 输入：1
# 输入任务：写作业
# 提示：任务已添加！
# 输入：2
# 输出：1. 写作业
# -----------------------------

# 制作提示：
# 1. 使用一个列表 `tasks = []` 存储数据。
# 2. 使用 `while` 循环展示菜单。
# 3. 对用户输入的菜单选项进行有效性检查（是不是1,2,3）。

# ========== 普通答案 ==========
tasks = []

def show_menu():
    print("\n=== 待办事项 ===")
    print("1. 添加任务")
    print("2. 查看任务")
    print("3. 退出")
    
def main():
    while True:
        show_menu()
        choice = input("请选择操作 (1-3): ")
        
        if choice == '1':
            task = input("请输入任务内容: ")
            if task.strip(): # 防止输入空内容
                tasks.append(task)
                print("✅ 任务已添加！")
            else:
                print("⚠️ 任务不能为空")
        elif choice == '2':
            if not tasks:
                print("📭 暂无任务，去添加一个吧！")
            else:
                for i, t in enumerate(tasks, 1):
                    print(f"{i}. {t}")
        elif choice == '3':
            print("👋 退出程序，再见！")
            break
        else:
            print("⚠️ 输入无效，请重新输入！")

if __name__ == "__main__":
    main()

