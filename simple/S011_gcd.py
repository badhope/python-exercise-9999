# -----------------------------
# 题目：求最大公约数。
# 描述：使用欧几里得算法求两个数的最大公约数。
# -----------------------------

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a

def main():
    print(f"gcd(48, 18) = {gcd(48, 18)}")
    print(f"gcd(100, 25) = {gcd(100, 25)}")


if __name__ == "__main__":
    main()
