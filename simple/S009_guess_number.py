# ========== 详细解析 ==========
# 导入 random 模块，用于生成随机数
import random

# 生成 1-100 之间的随机整数，包含边界值
# randint() 是闭区间函数，返回 [1, 100] 范围内的整数
target = random.randint(1, 100)

# 游戏开始提示
print("我想好了一个 1-100 的数字，来猜猜看！")

# 无限循环，直到用户猜对数字后通过 break 退出
while True:
    # 获取用户输入并转换为整数
    # input() 返回字符串，int() 进行类型转换
    guess = int(input("请输入你的猜测："))
    
    # 判断用户猜测与目标数字的关系
    if guess > target:
        print("大了，再小点！")
    elif guess < target:
        print("小了，再大点！")
    else:
        # 猜对时输出成功信息并退出循环
        print("恭喜你，猜对了！")
        break