# 题目：猜数字游戏。
# 描述：程序随机生成一个 1 到 100 的整数，用户输入猜测的数字。
#      程序提示"大了"、"小了"或"恭喜猜对"，直到猜对为止。
# -----------------------------

# 制作提示：
# 1. 需要引入 random 模块来生成随机数。
# 2. 使用 while 循环让游戏一直进行，直到猜对（break 跳出）。
# 3. 使用 input() 获取用户输入，记得转成整数 int()。
# 4. 使用 if-elif-else 判断大小关系。

# ========== 标准实现 ==========
import random

target = random.randint(1, 100)
print("我想好了一个 1-100 的数字，来猜猜看！")

while True:
    guess = int(input("请输入你的猜测："))
    
    if guess > target:
        print("大了，再小点！")
    elif guess < target:
        print("小了，再大点！")
    else:
        print("恭喜你，猜对了！")
        break

