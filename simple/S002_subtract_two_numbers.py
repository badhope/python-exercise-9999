# -----------------------------
# 题目：两数之差。
# 描述：给定两个数，返回它们的差。
# -----------------------------

def subtract_two_numbers(a, b):
    return a - b

def main():
    print(f"10 - 3 = {subtract_two_numbers(10, 3)}")
    print(f"5 - 8 = {subtract_two_numbers(5, 8)}")


if __name__ == "__main__":
    main()
