# -----------------------------
# 题目：两数之积。
# 描述：给定两个数，返回它们的乘积。
# -----------------------------

def multiply_two_numbers(a, b):
    return a * b

def main():
    print(f"3 * 5 = {multiply_two_numbers(3, 5)}")
    print(f"7 * 8 = {multiply_two_numbers(7, 8)}")


if __name__ == "__main__":
    main()
