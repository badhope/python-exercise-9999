# -----------------------------
# 题目：猜数字游戏。
# 描述：程序随机生成一个 1 到 100 的整数，用户输入猜测的数字。
#      程序提示“大了”、“小了”或“恭喜猜对”，直到猜对为止。
# -----------------------------

# 制作提示：
# 1. 需要引入 random 模块来生成随机数。
# 2. 使用 while 循环让游戏一直进行，直到猜对（break 跳出）。
# 3. 使用 input() 获取用户输入，记得转成整数 int()。
# 4. 使用 if-elif-else 判断大小关系。

# ========== 普通答案 ==========
import random

target = random.randint(1, 100)
print("我想好了一个 1-100 的数字，来猜猜看！")

while True:
    guess = int(input("请输入你的猜测："))
    
    if guess > target:
        print("大了，再小点！")
    elif guess < target:
        print("小了，再大点！")
    else:
        print("恭喜你，猜对了！")
        break

# ========== 运行效果 ==========
# 我想好了一个 1-100 的数字，来猜猜看！
# 请输入你的猜测：50
# 小了，再大点！
# 请输入你的猜测：80
# 大了，再小点！
# 请输入你的猜测：66
# 恭喜你，猜对了！

# ========== 详细解析版 ==========
# 1. 导入 random 工具包，这是 Python 自带的“抽奖箱”
import random

# 2. 生成一个 1 到 100 之间的随机整数，存到 target 变量里
# randint(1, 100) 是闭区间，包含 1 和 100
target = random.randint(1, 100)

# 3. 打印一句提示语
print("我想好了一个 1-100 的数字，来猜猜看！")

# 4. while True 表示“死循环”，只要不手动停止，它会一直跑下去
# 适合用在不知道具体循环次数的场景（比如不知道用户要猜几次）
while True:
    # 5. 获取用户输入，int() 把字符串强制转换成整数
    guess = int(input("请输入你的猜测："))
    
    # 6. 判断逻辑：如果猜的数字比目标大
    if guess > target:
        # 提示用户
        print("大了，再小点！")
    
    # 7. 否则如果猜的数字比目标小
    elif guess < target:
        # 提示用户
        print("小了，再大点！")
        
    # 8. 剩下的情况就是相等
    else:
        # 猜对了，打印恭喜
        print("恭喜你，猜对了！")
        # 9. break 是打破循环的锤子，执行到这里就会跳出 while 循环，游戏结束
        break

# ========== 大白话解释 ==========
# 这就像你在玩“冷热”游戏。
# target 是藏起来的宝藏位置，while True 是让你一直找下去。
# 每次你喊一个位置（input），裁判（if）会告诉你“离远了”或“离近了”。
# 直到你说对位置，裁判喊“正确”，然后 break 收工回家。

