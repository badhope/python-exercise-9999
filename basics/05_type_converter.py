# -----------------------------
# 题目：用户输入两个数字，计算它们的和。
# 重点：input 接收的都是字符串，需要转换类型。
# -----------------------------

# ========== 普通答案 ==========
num1 = input("输入第一个数字：")
num2 = input("输入第二个数字：")
print(int(num1) + int(num2))




# ========== 详细解析版 ==========
# 获取第一个数字的字符串形式，比如输入 "10"
num1 = input("输入第一个数字：")

# 获取第二个数字的字符串形式，比如输入 "20"
num2 = input("输入第二个数字：")

# int() 函数把字符串 "10" 变成整数 10
# 如果不转换，"10" + "20" 会变成 "1020" (字符串拼接)，而不是 30
print(int(num1) + int(num2))


# ========== 大白话解释 ==========
# input 拿到的东西默认都是“文字”（字符串）。
# 文字的 "1" 和 "1" 加在一起是 "11"，不是数学里的 2。
# 所以必须用 int() 把“文字”强行变成“数字”，才能做数学题。
