# -----------------------------
# 题目：位运算符练习。
# 描述：给定 a=12(1100), b=10(1010)，计算按位与&、按位或|、按位异或^、按位取反~、左移<<、右移>>。
# -----------------------------

def main():
    a = 12
    b = 10
    
    print(f"a = {a} (二进制: {bin(a)})")
    print(f"b = {b} (二进制: {bin(b)})")
    print(f"a & b (按位与): {a & b}")
    print(f"a | b (按位或): {a | b}")
    print(f"a ^ b (按位异或): {a ^ b}")
    print(f"~a (按位取反): {~a}")
    print(f"a << 2 (左移2位): {a << 2}")
    print(f"a >> 2 (右移2位): {a >> 2}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# &：两位都为1时结果为1
# |：任意一位为1时结果为1
# ^：两位不同时结果为1
# ~：按位取反
# <<：左移相当于乘以2的n次方
# >>：右移相当于除以2的n次方（向下取整）