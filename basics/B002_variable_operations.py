# -----------------------------
# 题目：定义变量并计算。
# 描述：创建两个整数变量 a=10, b=3，计算它们的和、差、积、商并输出。
# -----------------------------

def main():
    a = 10
    b = 3

    print(f"{a} + {b} = {a + b}")
    print(f"{a} - {b} = {a - b}")
    print(f"{a} * {b} = {a * b}")
    print(f"{a} / {b} = {a / b}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 变量定义：使用赋值运算符 = 创建变量
# 算术运算符：+ - * / 分别表示加减乘除
# 注意：/ 的结果为浮点数，即使整除也会得到浮点数结果