# -----------------------------
# 题目：询问用户的名字，然后打印“你好，[名字]！”。
# -----------------------------

# ========== 普通答案 ==========
name = input("请输入你的名字：")
print("你好，" + name)




# ========== 详细解析版 ==========
# input() 函数会在屏幕上显示括号里的提示语，然后停下来等你打字
# 你打完字按回车后，你输入的内容会被存到 name 变量里
name = input("请输入你的名字：")

# 用加号 "+" 把两个字符串拼在一起
# "你好，" 是固定的，name 是你刚才输入的
print("你好，" + name)


# ========== 大白话解释 ==========
# input 就像是程序伸出一只手，手里拿着张纸条写着问题，等你填空。
# 你填完的内容，它立马抓回来，存进变量里，下一行就能用了。
