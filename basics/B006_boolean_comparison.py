# -----------------------------
# 题目：布尔值和比较运算。
# 描述：创建变量 a=10, b=20，判断 a > b, a < b, a == b, a != b, a >= 10, b <= 20 的结果。
# -----------------------------

def main():
    a = 10
    b = 20
    
    print(f"a = {a}, b = {b}")
    print(f"a > b: {a > b}")
    print(f"a < b: {a < b}")
    print(f"a == b: {a == b}")
    print(f"a != b: {a != b}")
    print(f"a >= 10: {a >= 10}")
    print(f"b <= 20: {b <= 20}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 比较运算符返回布尔值：True 或 False
# == 判断相等，!= 判断不相等
# > < >= <= 用于数值比较