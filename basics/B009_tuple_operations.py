# -----------------------------
# 题目：元组基本操作。
# 描述：创建元组 point = (10, 20)，访问第一个和第二个元素，解包赋值给 x, y。
# -----------------------------

def main():
    point = (10, 20)
    
    print(f"元组: {point}")
    print(f"第一个元素: {point[0]}")
    print(f"第二个元素: {point[1]}")
    
    x, y = point
    print(f"解包: x = {x}, y = {y}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 元组是不可变的序列，用圆括号定义
# 支持索引访问和切片操作
# 可以进行元组解包：a, b = (1, 2)