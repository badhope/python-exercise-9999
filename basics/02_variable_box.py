# -----------------------------
# 题目：定义一个变量 name 存储你的名字，定义一个变量 age 存储你的年龄，并打印。
# -----------------------------

# ========== 普通答案 ==========
name = "Alex"
age = 20
print(name)
print(age)




# ========== 详细解析版 ==========
# 定义一个变量叫 name，把 "Alex" 这个字符串装进去
# 等号 = 不是相等，是“赋值”，把右边的东西给左边
name = "Alex"

# 定义一个变量叫 age，把数字 20 装进去
age = 20

# 打印 name 变量里装的东西
print(name)

# 打印 age 变量里装的东西
print(age)


# ========== 大白话解释 ==========
# 变量就像是一个贴了标签的快递盒子。
# "name" 是盒子上的标签，"Alex" 是盒子里装的手机。
# 当你把东西放进盒子（赋值）后，以后只要喊标签名字，就能拿出里面的东西。
