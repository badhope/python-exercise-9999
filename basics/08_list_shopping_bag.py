# -----------------------------
# 题目：创建一个列表存储三种水果，打印第二个水果，并在末尾添加一种新水果。
# -----------------------------

# ========== 普通答案 ==========
fruits = ["苹果", "香蕉", "橘子"]
print(fruits[1])
fruits.append("葡萄")
print(fruits)




# ========== 详细解析版 ==========
# 列表用方括号 [] 定义，里面可以装任何东西
fruits = ["苹果", "香蕉", "橘子"]

# 列表索引也是从0开始，第2个水果索引是1
print(fruits[1])  # 输出：香蕉

# append 是列表的专属技能，意思是“追加”
# 把 "葡萄" 放到列表的最后面
fruits.append("葡萄")

# 打印整个列表看看
print(fruits)


# ========== 大白话解释 ==========
# 列表就是一个购物袋，你可以往里面塞东西。
# 东西是有顺序的，拿的时候按位置拿（索引）。
# append 就像是往袋子最上面再扔进去一个东西。
