# -----------------------------
# 题目：字符串 s = "PYTHON"，获取第一个字符、最后一个字符、前三个字符。
# -----------------------------

# ========== 普通答案 ==========
s = "PYTHON"
print(s[0])
print(s[-1])
print(s[0:3])




# ========== 详细解析版 ==========
s = "PYTHON"

# 索引从0开始，[0] 表示第一个位置，即 'P'
print(s[0])

# 索引 -1 表示倒数第一个位置，即 'N'
print(s[-1])

# 切片 [起始:结束]，取第0个到第3个之前（不包含第3个）
# 也就是取索引 0, 1, 2 的内容 -> 'PYT'
print(s[0:3])


# ========== 大白话解释 ==========
# 字符串就像一排座位，编号从0开始。
# [0] 就是第一排，[-1] 就是倒数第一排。
# 切片 [0:3] 就像是切蛋糕，从第0刀切到第3刀，切出来的是中间那块，但不包含第3刀那个位置。
