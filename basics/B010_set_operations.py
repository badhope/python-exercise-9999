# -----------------------------
# 题目：集合基本操作。
# 描述：创建集合 set1 = {1, 2, 3, 4}，set2 = {3, 4, 5, 6}，求交集、并集、差集。
# -----------------------------

def main():
    set1 = {1, 2, 3, 4}
    set2 = {3, 4, 5, 6}
    
    print(f"set1: {set1}")
    print(f"set2: {set2}")
    print(f"交集: {set1 & set2}")
    print(f"并集: {set1 | set2}")
    print(f"差集(set1-set2): {set1 - set2}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 集合是无序且不重复的元素集合
# 交集 &：两个集合共有的元素
# 并集 |：合并所有元素（去重）
# 差集 -：属于set1但不属于set2的元素