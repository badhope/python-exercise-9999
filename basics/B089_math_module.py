import math

def main():
    print(f"pi: {math.pi}")
    print(f"e: {math.e}")
    print(f"sqrt(16): {math.sqrt(16)}")
    print(f"pow(2, 3): {math.pow(2, 3)}")
    print(f"factorial(5): {math.factorial(5)}")


if __name__ == "__main__":
    main()
