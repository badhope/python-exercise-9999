# -----------------------------
# 题目：嵌套循环打印九九乘法表。
# 描述：使用嵌套for循环打印九九乘法表。
# -----------------------------

def main():
    for i in range(1, 10):
        for j in range(1, i + 1):
            print(f"{j}*{i}={i*j}", end="\t")
        print()


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 外层循环控制行，内层循环控制列
# \t 制表符对齐输出
# print() 默认换行，end="" 取消自动换行