# -----------------------------
# 题目：运算符优先级练习。
# 描述：计算以下表达式的值并理解优先级：
# 1) 2 + 3 * 4
# 2) (2 + 3) * 4
# 3) 10 - 2 * 3 + 4
# 4) 2 ** 3 ** 2
# 5) 3 * 4 // 2
# -----------------------------

def main():
    print(f"2 + 3 * 4 = {2 + 3 * 4}")
    print(f"(2 + 3) * 4 = {(2 + 3) * 4}")
    print(f"10 - 2 * 3 + 4 = {10 - 2 * 3 + 4}")
    print(f"2 ** 3 ** 2 = {2 ** 3 ** 2}")
    print(f"3 * 4 // 2 = {3 * 4 // 2}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 优先级（从高到低）：** > * / // % > + -
# 括号()优先级最高
# **右结合：2**3**2 等价于 2**(3**2)