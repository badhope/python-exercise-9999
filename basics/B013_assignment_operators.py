# 题目：赋值运算符练习。
# 描述：使用复合赋值运算符简化运算：a=10，先+=5，再-=3，再*=2，再/=4，打印每步结果。
# -----------------------------

def main():
    a = 10
    print(f"初始值: a = {a}")
    
    a += 5
    print(f"a += 5: a = {a}")
    
    a -= 3
    print(f"a -= 3: a = {a}")
    
    a *= 2
    print(f"a *= 2: a = {a}")
    
    a /= 4
    print(f"a /= 4: a = {a}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 复合赋值运算符：+= -= *= /= //= %= **=
# a += 5 等价于 a = a + 5
# 这些运算符会修改原变量的值