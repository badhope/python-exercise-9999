# 题目：列表和元组转换。
# 描述：实现列表和元组之间的相互转换。
# -----------------------------

def main():
    lst = [1, 2, 3, 4, 5]
    tpl = (1, 2, 3, 4, 5)
    
    print(f"列表: {lst}")
    print(f"转元组: {tuple(lst)}")
    print(f"元组: {tpl}")
    print(f"转列表: {list(tpl)}")


if __name__ == "__main__":
    main()
