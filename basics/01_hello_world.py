# -----------------------------
# 题目：输出 "Hello, World!" 到控制台。
# -----------------------------

# ========== 普通答案 ==========
print("Hello, World!")




# ========== 详细解析版 ==========
# print() 是 Python 的内置函数，专门用来把括号里的东西“打印”到屏幕上
# "Hello, World!" 是一个字符串，用双引号包起来
print("Hello, World!")


# ========== 大白话解释 ==========
# 想象你在对着山谷喊话，print 就是你的嘴巴，括号里的话就是你要喊的内容。
# 只要运行这行代码，电脑就会把这句话显示在黑框框里。
