# 题目：算术运算符综合练习。
# 描述：给定 a=17, b=5，计算 a+b, a-b, a*b, a/b, a//b(整除), a%b(取余), a**b(幂运算)。
# -----------------------------

def main():
    a = 17
    b = 5
    
    print(f"a = {a}, b = {b}")
    print(f"加法 a + b = {a + b}")
    print(f"减法 a - b = {a - b}")
    print(f"乘法 a * b = {a * b}")
    print(f"除法 a / b = {a / b}")
    print(f"整除 a // b = {a // b}")
    print(f"取余 a % b = {a % b}")
    print(f"幂运算 a ** b = {a ** b}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# // 整除：向下取整的除法
# % 取余：返回除法后的余数
# ** 幂运算：a的b次方