# -----------------------------
# 题目：定义 name="Tom", score=95，使用两种方法输出 "Tom的分数是95"。
# 方法1：f-string (推荐)
# 方法2：format方法
# -----------------------------

# ========== 普通答案 ==========
name = "Tom"
score = 95

# 方法1
print(f"{name}的分数是{score}")

# 方法2
print("{}的分数是{}".format(name, score))




# ========== 详细解析版 ==========
name = "Tom"
score = 95

# 方法1：f-string
# 字符串前面写个 f，大括号 {} 里直接填变量名
# 这是目前最流行、最简洁的写法
print(f"{name}的分数是{score}")

# 方法2：format方法
# 字符串里挖两个坑 {}，后面用 .format() 把变量填进去
# 这是老版本的写法，但也很有用
print("{}的分数是{}".format(name, score))


# ========== 大白话解释 ==========
# 拼字符串就像填空题。
# f-string 就像你拿着笔直接在卷子上写答案，最直观。
# format 就像先留好空位，最后再把答案贴上去。
