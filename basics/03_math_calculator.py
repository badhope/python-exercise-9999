# -----------------------------
# 题目：计算 10 加 3 的结果，10 减 3 的结果，10 乘以 3 的结果，10 除以 3 的结果。
# -----------------------------

# ========== 普通答案 ==========
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)




# ========== 详细解析版 ==========
# 加法运算，直接打印结果 13
print(10 + 3)

# 减法运算，直接打印结果 7
print(10 - 3)

# 乘法运算，注意符号是星号 *，结果是 30
print(10 * 3)

# 除法运算，注意符号是斜杠 /，结果是 3.3333... (Python除法默认带小数)
print(10 / 3)


# ========== 大白话解释 ==========
# Python 可以直接当计算器用。
# 唯一要注意的是乘号是 "*"，除号是 "/"，这和数学课本上的写法不太一样，为了电脑输入方便。
