# -----------------------------
# 题目：字符串切片操作。
# 描述：对字符串 "Hello, World!" 进行切片：取前5个字符、取最后5个字符、取第7-11个字符。
# -----------------------------

def main():
    s = "Hello, World!"
    
    print(f"原字符串: {s}")
    print(f"前5个字符: {s[:5]}")
    print(f"最后5个字符: {s[-5:]}")
    print(f"第7-11个字符: {s[6:11]}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# 字符串切片语法：[start:end]，不包含end位置的字符
# 省略start表示从0开始，省略end表示到末尾
# 负数索引从字符串末尾开始计算