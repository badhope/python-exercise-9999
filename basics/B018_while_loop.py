# -----------------------------
# 题目：while循环和循环控制。
# 描述：使用while循环计算1到100的累加和，当和超过100时停止，打印最终和。
# -----------------------------

def main():
    total = 0
    i = 1
    
    while i <= 100:
        total += i
        if total > 100:
            break
        i += 1
    
    print(f"累加和超过100时停止，最终和: {total}, 最后加上的数: {i}")


if __name__ == "__main__":
    main()


# ========== 详细解析 ==========
# while循环：在条件为True时重复执行
# break：立即跳出循环
# continue：跳过本次循环，继续下一次
# 注意：while循环需要确保有退出条件，否则会死循环