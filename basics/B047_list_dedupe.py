# 题目：列表去重。
# 描述：去除列表 [1,2,2,3,3,3,4,4,5] 中的重复元素。
# -----------------------------

def main():
    lst = [1, 2, 2, 3, 3, 3, 4, 4, 5]
    print(f"原列表: {lst}")
    print(f"去重(set): {list(set(lst))}")
    print(f"保持顺序去重: {dict.fromkeys(lst).keys()}")


if __name__ == "__main__":
    main()
