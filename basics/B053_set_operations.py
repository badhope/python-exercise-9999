# 题目：集合操作。
# 描述：练习集合的添加、删除、交集、并集操作。
# -----------------------------

def main():
    s = {1, 2, 3}
    s.add(4)
    print(f"添加后: {s}")
    s.discard(2)
    print(f"删除后: {s}")
    
    s1 = {1, 2, 3}
    s2 = {2, 3, 4}
    print(f"s1: {s1}, s2: {s2}")
    print(f"交集: {s1 & s2}")
    print(f"并集: {s1 | s2}")
    print(f"差集: {s1 - s2}")


if __name__ == "__main__":
    main()
