# -----------------------------
# 题目：简易命令行计算器。
# 描述：用户输入两个数字和一个运算符（+ - * /），程序输出结果。
#      要求使用函数封装计算逻辑。
#
# 示例：
# 输入：5
# 输入：+
# 输入：3
# 输出：结果为: 8.0
# -----------------------------

# 制作提示：
# 1. 定义一个函数 calculate(a, b, operator)。
# 2. 函数内部根据 operator 的值做不同的运算。
# 3. 处理除数为 0 的特殊情况。

# ========== 普通答案 ==========
def calculate(a, b, op):
    if op == '+':
        return a + b
    elif op == '-':
        return a - b
    elif op == '*':
        return a * b
    elif op == '/':
        if b == 0:
            return "错误：除数不能为0"
        return a / b
    else:
        return "错误：不支持的运算符"

num1 = float(input("请输入第一个数字："))
operator = input("请输入运算符 (+ - * /)：")
num2 = float(input("请输入第二个数字："))

result = calculate(num1, num2, operator)
print(f"结果为: {result}")

# ========== 运行效果 ==========
# 请输入第一个数字：10
# 请输入运算符 (+ - * /)：/
# 请输入第二个数字：2
# 结果为: 5.0

# ========== 详细解析版 ==========
# 定义一个函数，接收三个参数：数字a，数字b，操作符op
# 这一步把“怎么算”的逻辑打包成一个工具
def calculate(a, b, op):
    # 如果操作符是加号
    if op == '+':
        return a + b
    # 如果是减号
    elif op == '-':
        return a - b
    # 如果是乘号
    elif op == '*':
        return a * b
    # 如果是除号，情况比较特殊
    elif op == '/':
        # 先检查除数 b 是不是 0
        if b == 0:
            # 如果是0，直接返回错误信息，不进行计算
            return "错误：除数不能为0"
        # 正常除法
        return a / b
    # 如果输入了乱七八糟的符号
    else:
        return "错误：不支持的运算符"

# 获取用户输入，转为 float 类型，方便处理小数
num1 = float(input("请输入第一个数字："))
operator = input("请输入运算符 (+ - * /)：")
num2 = float(input("请输入第二个数字："))

# 调用刚才定义的函数，把结果存到 result 里
result = calculate(num1, num2, operator)

# 打印最终结果
print(f"结果为: {result}")

# ========== 大白话解释 ==========
# 函数就像是一个自动售货机。
# 你把数字和符号从投币口塞进去（传参）。
# 机器内部有一套复杂的齿轮逻辑（if-elif判断）。
# 最后“啪”的一声，掉出来一罐可乐（return结果）。
# 封装好之后，你不用管机器内部怎么转，只管投币拿货就行。

